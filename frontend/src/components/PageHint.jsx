const PageHint = ({ pageKey }) => {
  const dismissed = loadDismissed();
  const [visible, setVisible] = useState(
    isHintsEnabled() && !dismissed.includes(pageKey)
  );

  if (!visible) return null;

  const hint = PAGE_HINTS[pageKey];
  if (!hint) return null;

  const handleClose = () => {
    setVisible(false);
    const updated = [...loadDismissed().filter((k) => k !== pageKey), pageKey];
    localStorage.setItem(DISMISSED_KEY, JSON.stringify(updated));
  };

  return (
    <div style={{ paddingInline: '32px', paddingTop: '16px' }}>
      <Collapse
        defaultActiveKey={['hint']}
        style={{ borderRadius: 14, border: '1px solid #ffe4ea', background: '#fff5f7' }}
        items={[
          {
            key: 'hint',
            label: (
              <span style={{ fontWeight: 600, color: '#ff8fab' }}>
                <BulbOutlined style={{ marginRight: 8 }} />
                {hint.title}
              </span>
            ),
            extra: (
              <CloseOutlined
                style={{ color: '#bbb', fontSize: 13 }}
                onClick={(e) => { e.stopPropagation(); handleClose(); }}
              />
            ),
            children: (
              <div style={{ display: 'flex', flexDirection: 'column', gap: 8 }}>
                {hint.items.map((item, i) => (
                  <div key={i} style={{ display: 'flex', gap: 12, alignItems: 'flex-start' }}>
                    <Tag
                      style={{
                        background: '#fff',
                        border: '1px solid #ffe4ea',
                        borderRadius: 8,
                        color: '#ff8fab',
                        fontWeight: 600,
                        flexShrink: 0,
                        marginTop: 1,
                        fontSize: 12,
                      }}
                    >
                      {item.label}
                    </Tag>
                    <span style={{ fontSize: 13, color: '#555', lineHeight: 1.5 }}>{item.desc}</span>
                  </div>
                ))}
              </div>
            ),
          },
        ]}
      />
    </div>
  );
};

export { isHintsEnabled };
export default PageHint;
